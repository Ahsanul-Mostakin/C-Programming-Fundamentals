#include <stdio.h>
int main() {
    float floatingPoint = 5.5;    
    int integer = 10;               
    char character = 'A';            
    double doublePrecision = 3.14159;

    printf("Integer: %d\n", integer);
    printf("Float: %.2f\n", floatingPoint);
    printf("Character: %c\n", character);
    printf("Double: %.5f\n", doublePrecision);

}
