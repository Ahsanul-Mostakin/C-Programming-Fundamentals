#include <stdio.h>

int main() {
    int age = 25;        
    float height = 5.9;          
    char grade = 'A';       
    double pi = 3.14;

    printf("Age: %d\n", age);
    printf("Height: %.1f\n", height);
    printf("Grade: %c\n", grade);
    printf("Value of Pi: %.10f\n", pi);

    return 0;
}
